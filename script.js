const themeLabel = document.querySelector("[data-theme-label]");
const langLabel = document.querySelector("[data-lang-label]");
const themeToggle = document.querySelector(".theme-toggle");
const langToggle = document.querySelector(".lang-toggle");

function applyTheme(theme) {
  document.documentElement.classList.toggle("theme-dark", theme === "dark");
  const lang = localStorage.getItem("language") || "es";
  themeLabel.textContent = theme === "dark" ? translations[lang].themeBright : translations[lang].themeDark;
  localStorage.setItem("theme", theme);
}

function applyLanguage(lang) {
  document.documentElement.lang = lang === "es" ? "es-DO" : "en";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = translations[lang][node.dataset.i18n] || node.textContent;
  });
  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    node.innerHTML = translations[lang][node.dataset.i18nHtml] || node.innerHTML;
  });
  const theme = localStorage.getItem("theme") || "bright";
  themeLabel.textContent = theme === "dark" ? translations[lang].themeBright : translations[lang].themeDark;
  langLabel.textContent = lang === "es" ? "EN" : "ES";
  localStorage.setItem("language", lang);
}

const initialLanguage = location.pathname.replace(/\/$/, "") === "/en" ? "en" : localStorage.getItem("language") || "es";

applyTheme(localStorage.getItem("theme") || "bright");
applyLanguage(initialLanguage);

themeToggle.addEventListener("click", () => {
  applyTheme(document.documentElement.classList.contains("theme-dark") ? "bright" : "dark");
});

langToggle.addEventListener("click", () => {
  applyLanguage((localStorage.getItem("language") || "es") === "es" ? "en" : "es");
});

function startAnimations() {
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { rootMargin: "0px 0px -12% 0px", threshold: 0.1 }
  );

  document.querySelectorAll(".reveal, .reveal-img").forEach((item) => {
    revealObserver.observe(item);
  });

  if (matchMedia("(hover: hover) and (pointer: fine)").matches) {
    const animatedItems = document.querySelectorAll(".service-images img, .work-img img");
    addEventListener(
      "scroll",
      () => {
        const viewport = innerHeight || document.documentElement.clientHeight;
        animatedItems.forEach((item) => {
          const rect = item.getBoundingClientRect();
          const progress = (rect.top - viewport) / (viewport + rect.height);
          item.style.transform = `translateY(${Math.max(-18, Math.min(18, progress * -28))}px)`;
        });
      },
      { passive: true }
    );
  }
}

if ("requestIdleCallback" in window) {
  requestIdleCallback(startAnimations, { timeout: 1600 });
} else {
  setTimeout(startAnimations, 350);
}
